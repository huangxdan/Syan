package com.app.sy.syan.data;

public class LoveInfo {
    private String id;
    private float loveFundMoney;
    private String updateTime;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public float getLoveFundMoney() {
        return loveFundMoney;
    }

    public void setLoveFundMoney(float loveFundMoney) {
        this.loveFundMoney = loveFundMoney;
    }

    public String getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(String updateTime) {
        this.updateTime = updateTime;
    }
}
